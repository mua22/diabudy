    public function ajax(Request $request)
    {
        $tags = array();
        if($request->has('term')){
            $tags = Tag::where('title','LIKE',"%{$request->term}%")->orderBy('title')->get();
        }else {
            $tags = Tag::orderBy('title')->get(array('title'));
        }
        return array_column($tags->toArray(),'title');


    }
